Object.freeze(STAGE_1_MANIFEST);

export function stage1Case(id: string): Stage1CaseDefinition {
  const found = STAGE_1_CASES.find((test) => test.id === id);
  if (!found) throw new Error(`unknown Stage 1 case: ${id}`);
  return found;
}
