export type ProbeKind =
  | "https"
  | "redirect"
  | "raw-http1"
  | "raw-http2"
  | "fault"
  | "revocation"
  | "confidentiality"
  | "bypass";

export interface Stage1CaseDefinition extends ConformanceCase {
  probe: Readonly<{
    kind: ProbeKind;
    scenario: string;
    expected: "allow" | "deny" | "safe" | "drain" | "redacted";
    positiveControl?: string;
  }>;
}

